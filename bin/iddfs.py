def dfs(start,goal,visited,lower_limit, upper_limit):
    if lower_limit > upper_limit:
        return False
    
    visited.append(start)
    if start ==goal:
        return True
    
    for city in dict_gn[start]:
        if city not in visited:
            found = dfs(city,goal,visited,lower_limit+1,upper_limit)
            if found:
                return True
            if visited:
                visited.pop()
    return None

def iddfs(start,goal,depth):
    for i in range(depth+1):
        print(f"Searching on Depth {i}")
        visited = []
        found = dfs(start,goal,visited,0,i)
        if found:
            print("->".join(visited))
            return
        else:
            print("Not found")

